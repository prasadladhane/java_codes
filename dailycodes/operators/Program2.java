class Demo2{
	public static void main(String[]args){
		//Logical Operators
		boolean x=true;
		boolean y=false;

		System.out.println("Logical AND:"+ (x&&y));
		System.out.println("Logical OR:"+ (x||y));
		System.out.println("Logical NOT X:"+ (!x));
		System.out.println("Logical NOT Y:"+ (!y));
	}
}



